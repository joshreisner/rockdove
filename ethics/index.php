<ol>
<li><b>Mission</b><br>
The primary mission of our practitioners is to enhance human well-being and help meet the basic human needs of all people, with particular attention to the needs and empowerment of people who are vulnerable, oppressed, and living in poverty. We emphasize a focus on individual well-being in a social context and the well-being of society. We attend to the environmental forces that create, contribute to, and address problems in living.
<br><br>
Practitioners promote social justice and social change with and on behalf of clients. "Clients" is used inclusively to refer to individuals, families, groups, organizations, and communities. Practitioners are sensitive to cultural and ethnic diversity and strive to end discrimination, oppression, poverty, and
other forms of social injustice. These activities may be in the form of
direct practice, community organizing, supervision, consultation,
administration, advocacy, social and political action, policy development and implementation, education, and research and
evaluation. Practitioners seek to enhance the capacity of people to
address their own needs. Practitioners also seek to promote the
responsiveness of organizations, communities, and other social institutions to individuals' needs and social problems.
<br><br>
