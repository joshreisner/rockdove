<li><b>The Rock Dove Collective & Mandatory Reporting</b><br>
While we understand and in part support the mandatory reporting of
abuse, particularly the abuse of children, we in the Rock Dove
Collective are not mandatory reporters.  Any and all information
shared with the collective will be kept in strict confidence and used
only to advocate for the person seeking aid.  The only exceptions are
cases in which an individual approaches us with plans to
injure another or with information about child abuse.  In such cases
we will exercise discretion and care for all parties and may inform
emergency and/or government services if we have reason to believe the
threat is imminent and no preferable option is available.
<br><br>
The Rock Dove Collective aims to serve in an advisory capacity to the
best of our ability in supporting the work of service providers within
our network, both for those who are mandatory reporters and for those
who are not.  However, we will leave it to the service providers
themselves to make a final decision about reporting any issues brought
directly to their attention.
<br><br>
As a future aspect of the Rock Dove Project, we hope to build a
directory of resources and information for children and families about
alternatives to entering the government system in situations of abuse.
