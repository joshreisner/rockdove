The language and content of this code is drawn primarily from the <a href="http://www.socialworkers.org/pubs/code/code.asp">Code of Ethics of the National Association of Social Workers</a>.  While our service providers represent a far broader range of skill, approach, expertise, and profession that just "social work," and while
our anti-capitalist views lead us to question certain aspects of the
code, on the whole we find these guidelines to be extremely useful,
appropriate, and grounding for our work.  (Areas of substantial divergence include: sexual & physical contact, payment for services, as well as
omissions that reflect the inapplicability of social work principles to
certain types of service providers.) What follows is an outline of key
principles we as a collective adopt to help us navigate the ethical waters of service provision.  You may also wish to read our <a href="/mission/">Mission Statement</a>.
