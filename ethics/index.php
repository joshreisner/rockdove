<li><b>Purpose of the Code</b><br>
The Code offers a set of values, principles, and standards to guide decision making and
conduct when ethical issues arise. It does not provide a set of rules
that prescribe how practitioners should act in all situations. Specific
applications of the Code must take into account the context in which it
is being considered and the possibility of conflicts among the Code's values, principles, and standards. Ethical responsibilities flow from all human relationships, from the personal and familial to the social and professional.
<br><br>
A code of ethics cannot guarantee ethical behavior. Moreover, a code of ethics cannot resolve all ethical issues or disputes or capture the richness and
complexity involved in striving to make responsible choices within a
moral community. Rather, a code of ethics sets forth values, ethical
principles, and ethical standards to which practitioners aspire and by which their actions can be guided. Practitioners' ethical behavior should result from their personal commitment to engage in ethical practice.</li>
<br><br>
<li><b>Ethical Principles</b><br>
The following broad ethical principles are based on practitioners' core
values of service, social justice, dignity and worth of the person,
importance of human relationships, integrity, and competence. 
<ul>
<li>&#8226; Service: Practitioners' primary goal is to help people in need and to address social problems.</li>
<li>&#8226; Social Justice: Practitioners challenge social injustice.</li>
<li>&#8226; Dignity and Worth of the Person: Practitioners respect the inherent dignity and worth of the person.</li>
<li>&#8226; Importance of Human Relationships: Practitioners recognize the central importance of human relationships.</li>
<li>&#8226; Integrity: Practitioners behave in a trustworthy manner.</li>
<li>&#8226; Competence: Practitioners practice within their areas of competence and develop and enhance their professional expertise.</li>
</ul>
<br>
<li><b>Ethical Standards</b>
<ul>
<li>&#8226; Commitment to Clients<br>
Practitioners' primary responsibility is to promote the well-being of clients. In general, clients' interests are primary. However,
practitioners' responsibility to the larger society or specific legal obligations may on limited occasions supersede the loyalty owed
clients, and clients should be so advised. (Examples include when a social worker is required by law to report that a client has abused a
child or has threatened to harm self or others.)
<br><br>
<li>&#8226; Self-Determination<br>
Practitioners respect and promote the right of clients to
self-determination and assist clients in their efforts to identify and
clarify their goals. Practitioners may limit clients' right to
self-determination when, in the practitioner's judgment, clients'
actions or potential actions pose a serious, foreseeable, and imminent
risk to themselves or others. See our <a
href="#mandatoryreporting">Policy on Mandatory Reporting</a>.
<br><br>
<li>&#8226; Informed Consent<br>
Practitioners should use clear and understandable language to inform
clients of the purpose of the services, risks related to the services,
limits to services because of the requirements of a third-party payer,
relevant costs, reasonable alternatives, clients' right to refuse or
withdraw consent, and the time frame covered by the consent.
Practitioners should provide clients with an opportunity to ask
questions. 
<br><br>
These conversations should be conducted in a language the client
understands, and translation/interpretation should be used when
necessary.
<br><br>
<li>&#8226; Competence<br>
Practitioners should provide services and represent themselves as competent only within the boundaries of their abilities.
<br><br>
<li>&#8226; Cultural Competence and Social Diversity<br>
Practitioners should understand culture and its function in human
behavior and society, recognizing and respecting the strengths that
exist in all cultures.
<br><br>
Practitioners should have a knowledge base of their clients' cultures
and be able to demonstrate competence in the provision of services that
are sensitive to clients' cultures and to differences among people and
cultural groups.
<br><br>
Practitioners should obtain education about and seek to understand the
nature of social diversity and oppression with respect to race,
ethnicity, national origin, color, sex, sexual orientation, age,
marital status, political belief, religion, and mental or physical
disability.
<br><br>
<li>&#8226; Conflicts of Interest<br>
Practitioners should be alert to and avoid conflicts of interest that
interfere with the exercise of professional discretion and impartial
judgment. Practitioners should inform clients when a real or potential
conflict of interest arises and take reasonable steps to resolve the
issue in a manner that makes the clients' interests primary and
protects clients' interests to the greatest extent possible. In some
cases, protecting clients' interests may require termination of the
professional relationship with proper referral of the client.
<br><br>
<li>&#8226; Privacy and Confidentiality<br>
Practitioners should respect clients' right to privacy. Practitioners
should not solicit private information from clients unless it is
essential to providing services or conducting social work evaluation or
research. Once private information is shared, standards of
confidentiality apply.
<br><br>
Practitioners should protect the confidentiality of all information
obtained in the course of service, except for compelling reasons. The
general expectation that practitioners will keep information
confidential does not apply when disclosure is necessary to prevent
serious, foreseeable, and imminent harm to a client or other
identifiable person. In all instances, practitioners should disclose
the least amount of confidential information necessary to achieve the
desired purpose; only information that is directly relevant to the
purpose for which the disclosure is made should be revealed, and
clients should be informed to the greatest extent possible of any
disclosures.
<br><br>
Practitioners should protect the confidentiality of clients to the greatest extent possible.
<br><br>
<li>&#8226; Sexual Relationships<br>
Practitioners should not engage in sexual activities or sexual contact
with clients, clients' relatives or other individuals with whom clients
maintain a close personal relationship when there is a risk of
exploitation or potential harm to the client. 
<br><br>
<li>&#8226; Physical Contact<br>
Practitioners should not engage in physical contact with clients when
there is a serious possibility of psychological harm to the client as a
result of the contact. Practitioners who engage in appropriate physical
contact with clients are responsible for setting clear, appropriate,
and culturally sensitive boundaries that govern such physical contact.
<br><br>
<li>&#8226; Sexual Harassment<br>
Practitioners should not sexually harass clients. Sexual harassment
includes sexual advances, sexual solicitation, requests for sexual
favors, and other verbal or physical conduct of a sexual 
<br><br>
<li>&#8226; Derogatory Language<br>
Practitioners should not use derogatory language in their written or
verbal communications to or about clients. Practitioners should use
accurate and respectful language in all communications to and about
clients.
<br><br>
<li>&#8226; Payment for Services<br>
Pracitioners should be clear and direct about what payment they expect
in return for their services, and should notify clients of any changes
in the agreement. Note: this is one area in which the Rock Dove
Collective differs substantially from the Social Workers Code of
Ethics, which discourages bartering or other inventive exchanges for
services. Such agreements of exchange and mutual aid are, in fact,
among our guiding principles.
<br><br>
<li>&#8226; Practitioners should collaborate respectfully with their
colleagues in the field in the interest of the clients, and should
support their colleagues' adherence to the code of ethics.
<br><br>
<li>&#8226; Relationships to Employers<br>
Practitioners should not allow an employing organization's policies,
procedures, regulations, or administrative orders to interfere with
their ethical practice of their work. Practitioners should act to
prevent and eliminate discrimination in the employing organization's
work assignments and in its employment policies and practices.
<br><br>
<li>&#8226; Anti-Discrimination<br>
Practitioners should not practice, condone, facilitate, or collaborate
with any form of discrimination on the basis of race, ethnicity,
national origin, color, sex, sexual orientation, age, marital status,
political belief, religion, or mental or physical disability.
<br><br>
<li>&#8226; Social Welfare<br>
Practitioners should promote the general welfare of society, from local
to global levels, and the development of people, their communities, and
their environments. Practitioners should advocate for living conditions
conducive to the fulfillment of basic human needs and should promote
social, economic, political, and cultural values and institutions that
are compatible with the realization of social justice.
<br><br>
<li>&#8226; Public Participation<br>
Practitioners should facilitate informed participation by the public in
shaping (or, Rock Dove would suggest, resisting, transforming, or
dismantling) social policies and institutions.
<br><br>
<li>&#8226; Social and Political Action<br>
Practitioners should engage in social and political action that seeks
to ensure that all people have equal access to the resources,
employment, services, and opportunities they require to meet their
basic human needs and to develop fully. 
<br><br>
Practitioners should act to expand choice and opportunity for all
people, with special regard for vulnerable, disadvantaged, oppressed,
and exploited people and groups.
<br><br>
Practitioners should promote conditions that encourage respect for
cultural and social diversity within the United States and globally.
Practitioners should promote policies and practices that demonstrate
respect for difference, support the expansion of cultural knowledge and
resources, advocate for programs and institutions that demonstrate
cultural competence, and promote policies that safeguard the rights of
and confirm equity and social justice for all people.
<br><br>
Practitioners should act to prevent and eliminate domination of,
exploitation of, and discrimination against any person, group, or class
on the basis of race, ethnicity, national origin, color, sex, sexual
orientation, age, marital status, political belief, religion, or mental
or physical disability.
</ul>
